interface LanguageProviderProps {
  children: ReactNode;
}

export const LanguageProvider: React.FC<LanguageProviderProps> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('myfans_language');
    return (saved as Language) || 'ja';
  });

  const setLanguage = (lang: Language) => {
    if (lang !== language) {
      setLanguageState(lang);
      localStorage.setItem('myfans_language', lang);
      // Force a re-render by updating the state
      // The page will automatically update without reload
    }
  };

  const t = (key: string): string => {
    const result = (translations[language] as Record<string, string>)[key] || key;
    // Debug logging
    if (language === 'ja' && key.startsWith('creator.')) {
      console.log(`JP Translation for "${key}":`, result);
    }
    return result;
  };

  useEffect(() => {
    document.documentElement.lang = language;
    console.log('Language changed to:', language);
  }, [language]);

  const value: LanguageContextType = {
    language,
    setLanguage,
    t
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export default LanguageContext;
